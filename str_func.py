def user_word(word: str):
    """

    :param word:
    :return: Возвращает слово с заглавными буквами.
    """
    return word.upper()


def capitalized_string(user_str: str):
    """

    :param user_str:
    :return: Возвращает заглавными первые буквы каждого слова в строке.
    """
    return user_str.capitalize()
